package com.ysk.source.entity;

/**
 * 常量表--实体类
 * 
 * @author admin
 *
 */
public class Constant {

	private String constantId;

	private String constantName;

	private String constantPrice;

	public String getConstantId() {
		return constantId;
	}

	public void setConstantId(String constantId) {
		this.constantId = constantId;
	}

	public String getConstantName() {
		return constantName;
	}

	public void setConstantName(String constantName) {
		this.constantName = constantName;
	}

	public String getConstantPrice() {
		return constantPrice;
	}

	public void setConstantPrice(String constantPrice) {
		this.constantPrice = constantPrice;
	}
}